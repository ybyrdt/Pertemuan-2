
package Pertemuan2;


public class Perulangan_FOR 
{
     public static void main(String[] args) {
        System.out.println("Angka genap dari 1 sampai 10:");

        for (int i = 1; i <= 20; i++) {
            if (i % 2 == 0) {
                System.out.println(i);
            }
        }
    }
}
